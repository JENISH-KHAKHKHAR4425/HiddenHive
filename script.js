function searchContent() {
  const input = document.getElementById("searchInput");

  if (!input) return;

  const value = input.value.toLowerCase().trim();
  const cities = ["rajkot", "junagadh", "surat", "ahmedabad", "vadodara"];

  if (value === "") {
    alert("Please enter something to search.");
    return;
  }

  const matchedCity = cities.find((city) => value.includes(city));

  if (matchedCity) {
    window.location.href = "city-details.html?city=" + encodeURIComponent(matchedCity);
  } else {
    window.location.href = "places.html";
  }
}

function filterPlaces() {
  const searchBox = document.getElementById("placeSearch");
  const cityFilter = document.getElementById("cityFilter");
  const categoryFilter = document.getElementById("categoryFilter");

  if (!searchBox || !cityFilter || !categoryFilter) return;

  const searchInput = searchBox.value.toLowerCase().trim();
  const cityValue = cityFilter.value;
  const categoryValue = categoryFilter.value;

  const placeCards = document.querySelectorAll(".place-item");
  const noResultsMessage = document.getElementById("noResultsMessage");

  let visibleCount = 0;

  placeCards.forEach((card) => {
    const title = card.querySelector("h3").innerText.toLowerCase();
    const city = card.getAttribute("data-city");
    const category = card.getAttribute("data-category");

    const matchesSearch = title.includes(searchInput);
    const matchesCity = cityValue === "all" || city === cityValue;
    const matchesCategory = categoryValue === "all" || category === categoryValue;

    if (matchesSearch && matchesCity && matchesCategory) {
      card.style.display = "block";
      visibleCount++;
    } else {
      card.style.display = "none";
    }
  });

  if (noResultsMessage) {
    noResultsMessage.style.display = visibleCount === 0 ? "block" : "none";
  }
}

function applyCityFromURL() {
  const params = new URLSearchParams(window.location.search);
  const city = params.get("city");

  if (!city) return;

  const cityFilter = document.getElementById("cityFilter");
  const title = document.getElementById("placesTitle");

  if (cityFilter) {
    cityFilter.value = city;
  }

  if (title) {
    title.innerText = "Hidden Places in " + city.charAt(0).toUpperCase() + city.slice(1);
  }

  filterPlaces();
}

function loadPlaceDetails() {
  const params = new URLSearchParams(window.location.search);
  const name = params.get("name");

  if (!name) return;

  const place = placeDetailsData.find((item) => item.name === name);

  if (!place) return;

  const placeName = document.getElementById("placeName");
  const placeCity = document.getElementById("placeCity");
  const placeCategory = document.getElementById("placeCategory");
  const placeDescription = document.getElementById("placeDescription");
  const placeTime = document.getElementById("placeTime");
  const placeTips = document.getElementById("placeTips");
  const placeImage = document.getElementById("placeImage");
  const gallerySection = document.getElementById("detailsGallerySection");
  const gallery = document.getElementById("detailsGallery");

  if (placeName) placeName.innerText = place.name;
  if (placeCity) placeCity.innerText = place.city;
  if (placeCategory) placeCategory.innerText = place.category;
  if (placeDescription) placeDescription.innerText = place.description;
  if (placeTime) placeTime.innerText = place.time;
  if (placeTips) placeTips.innerText = place.tips;

  if (placeImage) {
    placeImage.src = place.image;
    placeImage.alt = place.name;
  }

  if (gallerySection && gallery) {
    if (place.gallery && place.gallery.length > 0) {
      gallerySection.style.display = "block";
      gallery.innerHTML = place.gallery
        .map(
          (image, index) => `
            <div class="gallery-card">
              <img src="${image}" alt="${place.name} photo ${index + 1}">
            </div>
          `
        )
        .join("");
    } else {
      gallerySection.style.display = "none";
      gallery.innerHTML = "";
    }
  }
}

